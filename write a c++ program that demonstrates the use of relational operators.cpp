using namespace std;
#include<iostream>
int main()
{
	int x=10,y=20;
	cout<<"\n"<<x<<"<"<<y<<"="<<(x<y);
	cout<<"\n"<<x<<"=="<<y<<"="<<(x==y);
	cout<<"\n"<<x<<"!"<<y<<"="<<(x!=y);
	cout<<"\n"<<x<<">"<<y<<"="<<(x>y);
	cout<<"\n"<<x<<">="<<y<<"="<<(x>=y);
	cout<<"\n"<<x<<"<="<<y<<"="<<(x<=y);
	return 0;
}
